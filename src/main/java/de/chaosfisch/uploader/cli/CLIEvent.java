package de.chaosfisch.uploader.cli;

public class CLIEvent {

	private final String key;
	private final String value;

	public CLIEvent(final String key, final String value) {
		this.key = key;
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
}
