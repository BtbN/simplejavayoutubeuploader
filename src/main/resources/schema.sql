    MONETIZE_TRUEVIEW BOOLEAN DEFAULT FALSE NOT NULL,
    MONETIZE_INSTREAM BOOLEAN DEFAULT FALSE NOT NULL,
    MONETIZE_PRODUCT BOOLEAN DEFAULT FALSE NOT NULL,
    MONETIZE_SYNDICATION VARCHAR(255),
